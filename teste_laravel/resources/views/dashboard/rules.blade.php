    <h3>Regras gerais</h3>
    <br/>
    <p>
      O Quotas é um sistema de recompensas em que permite você trocar pontos acumulados mensalmente por prêmios surpreendentes.
      Para participar é fácil: cada colaborador do CSC (Centro de Serviços Compartilhados) terá creditado mensalmente em sua conta, todo dia 05,  “200 Quotas”. Para manter e acumular as quotas basta o colaborador obedecer regras de boa conduta estabelecidas pela empresa. Existindo eventos em que o colaborador não cumpra com tais regras, ocorrerá o débito das quotas proporcionalmente a gravidade do evento. Em contrapartida, caso o colaborador mantenha um histórico de boa conduta, sem débitos, poderá rapidamente trocar as quotas acumuladas por prêmios disponíveis no sistema na página “Resgate”.
    </p>
    <br/>
    <h4>Como funciona o lançamento dos débitos?</h4>
    <p>
      Se um colaborador observar alguma ação irregular por parte de outros colaboradores, poderá abrir o chamado QO.001 no Workflow. O chamado será anônimo e passará pelo crivo de um grupo de aprovadores. Uma vez constatada a irregularidade ou ação de má conduta, o administrador do sistema realizará o lançamento do débito na conta do colaborador.
    </p>
    <br/>
    <h4>Ações que geram débitos:</h4>
    <ul>
      <li>Atraso em reunião: a partir de 5 minutos de atraso será debitado 60 Quotas;</li>
      <li>Falta em reunião não justificada: quando um colaborador faltar em uma reunião e não justificar aos responsáveis será debitado 100 Quotas;</li>
      <li>Não responder o Educaquiz no mês: será debitado 100 Quotas;</li>
      <li>Solucionar chamado sem devido atendimento ou feedback ao solicitante: débito de 200 Quotas aos colaboradores que solucionarem chamados sem a resolução do problema;</li>
      <li>Falta ao trabalho não justificada: se um colaborador faltar ao trabalho sem justificativa, será debitado 100 Quotas;</li>
      <li>Comer na estação de trabalho: quando um colaborador comer em local de trabalho, isto é, em sua mesa ou em salas de reunião, será debitado;</li>
      <li>Programa 5S: será feita auditoria periódica e observação por parte dos demais colaboradores sobre a organização e limpeza da estação de trabalho. Caso não esteja conforme o Programa 5S, será debitado 100 Quotas;</li>
      <li>Estacionar em local não permitido para funcionários: caso um colaborador estacione nas vagas que não são destinadas aos funcionários, como as vagas de visitantes ou vagas de gerências, será debitado 20 Quotas;</li>
      <li>Hora extra sem autorização: o colaborador só poderá fazer hora extra se o gestor da área autorizar. Caso contrário, será debitado 100 Quotas;</li>
      <li>Conduta de desperdício: para desperdícios de água, luz e ar condicionado será debitado 60 Quotas.</li>
    </ul>
